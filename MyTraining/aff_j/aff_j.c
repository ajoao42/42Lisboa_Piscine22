#include <unistd.h>

int	main(int argc, char *argv[])
{
	int i;

	i = 0;
	if(argc != 1)
	{
		return (write(1, "j\n", 2), 0);
	}
	while (argv[1][i] != '\0')
	{
		if (argv[1][i] == 'j')
		{
			write(1, "j", 1);
			break;
		}
		i++;
	}
	return (write(1, "\n", 2), 0);
}
